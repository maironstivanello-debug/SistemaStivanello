package bean;

public class Especificacao {

    private int mmsIdEspecificacao;
    private String mmsTamanho;
    private String mmsCor;
    private String mmsMarca;
    private String mmsMaterial;
    private String mmsTipo;
    private String mmsGenero;
    private String mmsTipoSolado;

    public Especificacao() {
    }

    public int getMmsIdEspecificacao() {
        return mmsIdEspecificacao;
    }

    public void setMmsIdEspecificacao(int mmsIdEspecificacao) {
        this.mmsIdEspecificacao = mmsIdEspecificacao;
    }

    public String getMmsTamanho() {
        return mmsTamanho;
    }

    public void setMmsTamanho(String mmsTamanho) {
        this.mmsTamanho = mmsTamanho;
    }

    public String getMmsCor() {
        return mmsCor;
    }

    public void setMmsCor(String mmsCor) {
        this.mmsCor = mmsCor;
    }

    public String getMmsMarca() {
        return mmsMarca;
    }

    public void setMmsMarca(String mmsMarca) {
        this.mmsMarca = mmsMarca;
    }

    public String getMmsMaterial() {
        return mmsMaterial;
    }

    public void setMmsMaterial(String mmsMaterial) {
        this.mmsMaterial = mmsMaterial;
    }

    public String getMmsTipo() {
        return mmsTipo;
    }

    public void setMmsTipo(String mmsTipo) {
        this.mmsTipo = mmsTipo;
    }

    public String getMmsGenero() {
        return mmsGenero;
    }

    public void setMmsGenero(String mmsGenero) {
        this.mmsGenero = mmsGenero;
    }

    public String getMmsTipoSolado() {
        return mmsTipoSolado;
    }

    public void setMmsTipoSolado(String mmsTipoSolado) {
        this.mmsTipoSolado = mmsTipoSolado;
    }
}